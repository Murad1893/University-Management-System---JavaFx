package application;

public class StudentMarks {	
	String id, marks;

	public StudentMarks(String id, String marks) {
		super();
		this.id = id;
		this.marks = marks;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getMarks() {
		return marks;
	}

	public void setMarks(String marks) {
		this.marks = marks;
	}
	
	
}
