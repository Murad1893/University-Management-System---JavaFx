package application;

public class warningclass {
	String id,name,warning;

	public warningclass(String id, String name, String warning) {
		super();
		this.id = id;
		this.name = name;
		this.warning = warning;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getWarning() {
		return warning;
	}

	public void setWarning(String warning) {
		this.warning = warning;
	}


	

}
